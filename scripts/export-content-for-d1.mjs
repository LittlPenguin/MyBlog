import { readdir, readFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import matter from "gray-matter";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const contentRoot = path.join(rootDir, "src", "content");

const collections = {
  posts: path.join(contentRoot, "posts"),
  projects: path.join(contentRoot, "projects"),
  resources: path.join(contentRoot, "resources"),
};

function sqlString(value) {
  if (value === null || value === undefined) {
    return "NULL";
  }

  return `'${String(value).replaceAll("'", "''")}'`;
}

async function readCollection(collection, directory) {
  const entries = await readdir(directory, { withFileTypes: true }).catch(() => []);
  const files = entries
    .filter((entry) => entry.isFile() && entry.name.endsWith(".mdx"))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));

  return Promise.all(
    files.map(async (fileName) => {
      const source = await readFile(path.join(directory, fileName), "utf8");
      const { data, content } = matter(source);
      const slug = typeof data.slug === "string" && data.slug ? data.slug : fileName.replace(/\.mdx$/, "");
      const date = typeof data.date === "string" && data.date ? data.date : new Date().toISOString().slice(0, 10);
      const tags = Array.isArray(data.tags) ? data.tags.filter((tag) => typeof tag === "string") : [];
      const frontmatter = {
        ...data,
        slug,
        tags,
      };

      return {
        collection,
        slug,
        title: typeof data.title === "string" ? data.title : slug,
        summary: typeof data.summary === "string" ? data.summary : "",
        category: typeof data.category === "string" ? data.category : "",
        tags,
        frontmatter,
        body: content,
        source,
        publishedAt: `${date.slice(0, 10)}T00:00:00.000Z`,
        updatedAt: new Date().toISOString(),
      };
    }),
  );
}

const rows = (
  await Promise.all(
    Object.entries(collections).map(([collection, directory]) => readCollection(collection, directory)),
  )
).flat();

console.log("-- Generated by scripts/export-content-for-d1.mjs");

for (const row of rows) {
  console.log(`INSERT INTO content_items (
  collection, slug, title, summary, category, tags_json, frontmatter_json, body, source, published_at, updated_at, deleted_at
) VALUES (
  ${sqlString(row.collection)},
  ${sqlString(row.slug)},
  ${sqlString(row.title)},
  ${sqlString(row.summary)},
  ${sqlString(row.category)},
  ${sqlString(JSON.stringify(row.tags))},
  ${sqlString(JSON.stringify(row.frontmatter))},
  ${sqlString(row.body)},
  ${sqlString(row.source)},
  ${sqlString(row.publishedAt)},
  ${sqlString(row.updatedAt)},
  NULL
) ON CONFLICT(collection, slug) DO UPDATE SET
  title = excluded.title,
  summary = excluded.summary,
  category = excluded.category,
  tags_json = excluded.tags_json,
  frontmatter_json = excluded.frontmatter_json,
  body = excluded.body,
  source = excluded.source,
  published_at = excluded.published_at,
  updated_at = excluded.updated_at,
  deleted_at = NULL;`);
}
