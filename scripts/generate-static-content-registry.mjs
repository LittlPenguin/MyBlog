import { readdir, readFile, mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const contentRoot = path.join(rootDir, "src", "content");
const outputPath = path.join(contentRoot, "generated", "static-content-registry.ts");
const jsOutputPath = path.join(contentRoot, "generated", "static-content-registry.js");
const typesOutputPath = path.join(contentRoot, "generated", "static-content-registry.d.ts");

const collections = {
  posts: path.join(contentRoot, "posts"),
  projects: path.join(contentRoot, "projects"),
  resources: path.join(contentRoot, "resources"),
};

async function readCollection(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = entries
    .filter((entry) => entry.isFile() && entry.name.endsWith(".mdx"))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));

  return Promise.all(
    files.map(async (fileName) => ({
      fileName,
      source: await readFile(path.join(directory, fileName), "utf8"),
    })),
  );
}

const registry = Object.fromEntries(
  await Promise.all(
    Object.entries(collections).map(async ([collection, directory]) => [
      collection,
      await readCollection(directory),
    ]),
  ),
);

const source = `// This file is generated by scripts/generate-static-content-registry.mjs.
// It lets Cloudflare Workers read bundled MDX content without relying on runtime fs access.

export type StaticContentCollection = "posts" | "projects" | "resources";

export type StaticContentEntry = {
  fileName: string;
  source: string;
};

export const staticContentRegistry = ${JSON.stringify(registry, null, 2)} as const satisfies Record<
  StaticContentCollection,
  readonly StaticContentEntry[]
>;
`;

const jsSource = `// This file is generated by scripts/generate-static-content-registry.mjs.
// It lets Cloudflare Workers read bundled MDX content without relying on runtime fs access.

export const staticContentRegistry = ${JSON.stringify(registry, null, 2)};
`;

const typesSource = `// This file is generated by scripts/generate-static-content-registry.mjs.

export type StaticContentCollection = "posts" | "projects" | "resources";

export type StaticContentEntry = {
  fileName: string;
  source: string;
};

export declare const staticContentRegistry: Record<
  StaticContentCollection,
  readonly StaticContentEntry[]
>;
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, source, "utf8");
await writeFile(jsOutputPath, jsSource, "utf8");
await writeFile(typesOutputPath, typesSource, "utf8");
